#include <starpu.h>
#include "../helper.h"

int main(void)
{
	int ret;

	ret = starpu_init(NULL);
	if (ret == -ENODEV) return STARPU_TEST_SKIPPED;
	STARPU_CHECK_RETURN_VALUE(ret, "starpu_init");

	float *buffer = NULL;
	starpu_free_flags(buffer, 0, STARPU_MALLOC_PINNED|STARPU_MALLOC_COUNT);

	starpu_shutdown();
	return 0;
}
